# 3. Создайте словарь со списком вещей для похода в качестве ключа и их массой в качестве значения.
# Определите какие вещи влезут в рюкзак передав его максимальную грузоподъёмность.
# Достаточно вернуть один допустимый вариант.
# *Верните все возможные варианты комплектации рюкзака.

list_items = {
    "Палатка": 2.5,
    "Спальный мешок": 1.5,
    "Коврик": 0.5,
    "Горелка": 0.5,
    "Кастрюля": 3.0,
    "Термос": 1.0,
    "Фонарик": 0.4,
    "Компас": 0.3,
    "Нож": 0.5,
    "Первая помощь": 0.8,
}

max_weight = 7
total_weight = 0
backpack = []

for item in list_items:
    if total_weight + list_items[item] <= max_weight:
        backpack.append(item)
        total_weight += list_items[item]

print(f"В рюкзак влезут следующие вещи:\n{backpack}")
